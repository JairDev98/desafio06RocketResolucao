import {
  Entity,
  Column,
  PrimaryGeneratedColumn,
  ManyToOne,
  JoinColumn,
  CreateDateColumn,
  UpdateDateColumn,
} from 'typeorm';

// IMPORTANDO CATEGORIA PARA SER UTILIZADA PARA LINKAR A FK
import Category from './Category';

@Entity('transactions')
class Transaction {
  @PrimaryGeneratedColumn('uuid')
  id: string;

  @Column()
  title: string;

  @Column()
  type: 'income' | 'outcome';

  @Column('decimal')
  value: number;

  // AQUI FOI EFETUADO UM LINK PARA QUE ESSA COLUNA CATEGORY_ID TENHA O MODELO DA CATEGORY DO MODEL
  // quando passado uma categoria ele vai fazer o referenciamento a esse relacionamento e ir para a coluna category_id
  @ManyToOne(() => Category)
  @JoinColumn({ name: 'category_id' })
  category: Category;

  @Column()
  category_id: string;

  @CreateDateColumn()
  created_at: Date;

  @UpdateDateColumn()
  updated_at: Date;
}

export default Transaction;
